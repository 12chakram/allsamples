package biz.neustar.dece.portal.saml.exception;

public class MIDSPostException extends Exception
{
	public MIDSPostException()
	{
	}

	public MIDSPostException(String message)
	{
		super(message);
	}

	public MIDSPostException(Throwable cause)
	{
		super(cause);
	}

	public MIDSPostException(String message, Throwable cause)
	{
		super(message, cause);
	}

}
